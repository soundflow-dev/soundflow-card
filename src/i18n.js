export function getLang(hass) {
  const lang = hass?.locale?.language || hass?.language || 'en';
  return lang.toLowerCase().startsWith('pt') ? 'pt' : 'en';
}

export function t(hass, key, vars) {
  const lang = getLang(hass);
  const dict = STRINGS[lang] || STRINGS.en;
  let s = dict[key] ?? STRINGS.en[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), vars[k]);
    }
  }
  return s;
}

export function plural(hass, n, oneKey, manyKey) {
  return n === 1 ? t(hass, oneKey) : t(hass, manyKey, { n });
}
